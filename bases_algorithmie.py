# Affichage des nombres entiers pairs entre 0 et 10 inclus
print("Nombres entiers pairs :")
for i in range(11):
    if i % 2 == 0:
        print(i)

# Affichage des nombres entiers impairs entre 0 et 10 inclus
print("\nNombres entiers impairs :")
for i in range(11):
    if i % 2 != 0:
        print(i)
