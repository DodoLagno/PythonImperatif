# ex01_affichageListe.py

liste = [1, 15, -3, 0, 8, 7, 4, -2, 28, 7, -1, 17, 2, 3, 0, 14, -4]

# Affichage des éléments de la liste grâce à une boucle
for element in liste:
    print(element)

# Affichage des éléments de la liste dans l'ordre inverse
for element in reversed(liste):
    print(element)
